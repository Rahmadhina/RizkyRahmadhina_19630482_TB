    private void rr_ubahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rr_ubahActionPerformed
        int ok=JOptionPane.showConfirmDialog(null,"Apakah yakin ingin mengedit data?","Confirmation",
                JOptionPane.YES_NO_OPTION);
        nis=rr_nis.getText();
        nama=rr_nama.getText();
        alamat=rr_alamat.getText();
        jk=rr_jk.getItemAt(rr_jk.getSelectedIndex()).toString();
        agama=rr_agama.getItemAt(rr_agama.getSelectedIndex()).toString();
        tanggal=rr_asalsekolah.getText();
        telepon=rr_telepon.getText();
        asal=rr_asalsekolah.getText();
        nilai=rr_nilaiakhir.getText();
        try {
            String sql=" update  siswa "
            + "set nama='"+ nama +"',"
            + "alamat='"+ alamat +"',"
            + "jenis_kelamin='"+ jk +"',"
            + "agama='"+ agama +"',"
            + "tanggal_lahir='"+ tanggal +"',"
            + "telepon='"+ telepon +"',"
            + "asal_sekolah='"+ asal +"',"
            + "nilai_akhir='"+ nilai
            + "' where nis='"+ nis +"'";
            stat=con.createStatement();
            stat.execute(sql);
            new Siswa().setVisible(true);
            dispose();
            JOptionPane.showMessageDialog(null,"Data Berhasil di edit");
        } catch (Exception e) {
            JOptionPane.showConfirmDialog(null, "Tidak Ada Data Yang Di Edit ....!! \n"
                +e.getMessage());
        }
    }//GEN-LAST:event_rr_ubahActionPerformed

