    private void id()
    {
       try {
            String sql="select * from siswa order by nis desc";
            stat=con.createStatement();
            res=stat.executeQuery(sql);
            if (res.next()) {
                String id = res.getString("nis").substring(1);
                String AN = "" + (Integer.parseInt(id) + 1);
                String Nol = "";

                if(AN.length()==1)
                {Nol = "000";}
                else if(AN.length()==2)
                {Nol = "00";}
                else if(AN.length()==3)
                {Nol = "0";}
                else if(AN.length()==4)
                {Nol = "";}

               rr_nis.setText("N" + Nol + AN);
            } else {
               rr_nis.setText("N0211");
            }

           }catch(Exception e){
           JOptionPane.showMessageDialog(null, e);
           }
     }
    
    

